public class TypeCasting {
    public static void main(String[] args) {


        // Explicit TypeCasting
        double d1 = 99.999999943;
        int d2 = (int) d1;   //double converted to int data type
        System.out.println(d2);

        int a = 66;
        char ch = (char) a;  //int converted to char data type
        System.out.println(ch);
    }
}
